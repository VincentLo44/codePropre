package ex1;

public class Calcul {

	/**
	 * Permet d'additionner 2 entiers
	 * 
	 * @param a int 1
	 * @param b int 2
	 * @return int
	 */
	public int addition(int a, int b) {
		return a + b;
	}

	/**
	 * Permet de soustraire 2 entiers
	 * 
	 * @param a int 1
	 * @param b int 2
	 * @return int
	 */
	public int soustraction(int a, int b) {
		return a - b;
	}
}
